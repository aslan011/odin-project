def add a,b
    return a + b
end

def subtract a,b
    return a - b
end

def sum (args)
    total = 0
    args.each do |number|
        total = total + number
    end
    return total
end

def multiply (args)
        total = 1
        args.each do |number|
            total = total * number
        end
        return total
end

def power a,b
    return  a ** b   
end
        

